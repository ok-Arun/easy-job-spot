package com.easyjobspot.backend.enums;

public enum ApplicationStatus {

    /**
     * Candidate has applied for the job
     */
    APPLIED,

    /**
     * Admin shortlisted the candidate
     */
    SHORTLISTED,

    /**
     * Candidate rejected by admin
     */
    REJECTED,

    /**
     * Candidate finally selected
     */
    HIRED
}
